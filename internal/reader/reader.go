type Config struct {
    // RPCURL          string
	Client *ethclient.Client
    ContractAddress string
    CustomABI       string // Optional: provide a custom ABI instead of using the default ERC20
}



// create new instance of reader

func New(cfg Config) (*Reader, error) {
	abiString := erc20ABI

	parsedABI, err := abi.JSON(strings.NewReader(abiString))
	if err != nil {
		return nil, fmt.Errorf(("failed to parse ABI: %w"), err)
	}

	address := common.HexToAddress(cfg.ContractAddress)

	return &Reader {
		client: cfg.Client,
		address: address,
		abi: parsedABI,
	}, nil
}

func (r *Reader) Close() {
	r.client.Close()
}

func (r *Reader) ReadTokenName(ctx context.Context) (string, error) {
	var result string

	data, err := r.abi.Pack("name")
	if err != nil {
		return result, fmt.Errorf("failed to pack data: %w", err)
	}

	msg := ethereum.CallMsg{
		To:   &r.address,
		Data: data,
	}


	output, err := r.client.CallContract(ctx, msg, nil)
	if err != nil {
		return result, fmt.Errorf("failed to call contract: %w", err)
	}

	err = r.abi.UnpackIntoInterface(&result, "name", output)
	if err != nil {
		return result, fmt.Errorf("failed to unpack data: %w", err)
	}

	return result, nil
}